#==============================================================================
# ** Window_Resource
#------------------------------------------------------------------------------
#  This window displays the amount of resources.
#==============================================================================

class Window_Resource < Window_Base
  #--------------------------------------------------------------------------
  # * Object Initialization
  #     x : window X coordinate
  #     y : window Y coordinate
  #--------------------------------------------------------------------------
  def initialize(x, y)
    super(x, y, 160, WLH + 32)
    refresh
  end
  #--------------------------------------------------------------------------
  # * Refresh
  #--------------------------------------------------------------------------
  def refresh
    self.contents.clear
    draw_resource_value($game_variables[6], 4, 0, 120)
  end
end
